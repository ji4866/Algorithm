#\n이건 줄을 넘기는 문자인데 이런 문자들을 메타 문자라고 합니다. 단순 문자가 아닌 다른 용도로 사용되는 문자를 말하죠.
#여기서 백슬래시는(\) 이런 메타문자를 사용할때 사용하는 문자이기 때문에 그냥 \이렇게 입력하면 문자라고 인식하지 않습니다.
#그래서 이런 의미를 가진 문자를 출력하고 싶을 때는 앞에 백슬래시를 하나 붙여서 \\입력하셔야 원하는 문자가 출력됩니다.
#" 이것도 문자열의 시작과 끝을 알리는 문자이기 때문에 이 문자를 출력하고 싶을때는 마찬가지로 백슬래시를 붙여써야 합니다.

print("!@#$%^&*(\\'\"<>?:;")